<template>
  <v-dialog
    :value="value"
    :max-width="500"
    @input="$emit('input', $event)"
  >
    <v-form
      ref="form"
      v-model="valid"
      @submit.prevent="handleSave(user)"
    >
      <v-card>
        <v-card-title class="card-heading py-2">
          <span class="focus--text">{{ (user.created_on) ? $t('app.general.label.edit_user') : $t('app.general.label.add_user') }}</span>
        </v-card-title>

        <v-divider />

        <app-setting :title="$t('app.general.label.name')">
          <v-text-field
            v-model="user.username"
            autocomplete="username"
            :disabled="(user.created_on)"
            filled
            dense
            class="mt-0"
            hide-details="auto"
            :rules="[
              $rules.required,
              $rules.lengthLessThanOrEqual(60)
            ]"
          />
        </app-setting>

        <v-divider />

        <app-setting :title="$t('app.general.label.password')">
          <v-text-field
            v-model="user.password"
            autocomplete="current-password"
            filled
            dense
            type="password"
            class="mt-0"
            hide-details="auto"
            :rules="[
              $rules.required,
              $rules.lengthGreaterThanOrEqual(4),
              $rules.passwordNotEqualUsername(user.username)
            ]"
          />
        </app-setting>

        <v-divider />

        <v-card-actions>
          <v-spacer />
          <app-btn
            color="warning"
            text
            type="button"
            @click="$emit('input', false)"
          >
            {{ $t('app.general.btn.cancel') }}
          </app-btn>
          <app-btn
            color="primary"
            type="submit"
          >
            {{ (user.created_on) ? $t('app.general.btn.save') : $t('app.general.btn.add') }}
          </app-btn>
        </v-card-actions>
      </v-card>
    </v-form>
  </v-dialog>
</template>

<script lang="ts">
import { Component, Vue, Prop } from 'vue-property-decorator'
import { AppUser } from '@/store/auth/types'

@Component({})
export default class UserConfigDialog extends Vue {
  @Prop({ type: Boolean, required: true })
  readonly value!: boolean

  @Prop({ type: Object, required: true })
  readonly user!: AppUser

  valid = false

  handleSave () {
    if (this.valid) {
      this.$emit('save', this.user)
      this.$emit('input', false)
    }
  }
}
</script>
